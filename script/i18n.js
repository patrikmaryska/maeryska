function t(key) {
    const lang = document.documentElement.getAttribute('data-lang') || 'en';
    return (translations[lang] && translations[lang][key]) != null
        ? translations[lang][key]
        : (translations['en'][key] != null ? translations['en'][key] : key);
}

function setLanguage(lang) {
    document.documentElement.setAttribute('data-lang', lang);
    document.documentElement.setAttribute('lang', lang === 'cz' ? 'cs' : 'en');

    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        el.textContent = t(el.getAttribute('data-i18n'));
    });

    document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
        el.innerHTML = t(el.getAttribute('data-i18n-html'));
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });

    if (typeof renderFeatured === 'function') renderFeatured();
    if (typeof renderNews === 'function') renderNews();
    if (typeof renderSongs === 'function') renderSongs();
    if (typeof renderGallery === 'function') renderGallery();

    var themeBtn = document.getElementById('theme-toggle');
    if (themeBtn) {
        var currentTheme = document.documentElement.getAttribute('data-theme') || 'light';
        var themeSpan = themeBtn.querySelector('span');
        if (themeSpan) themeSpan.textContent = currentTheme === 'dark' ? t('theme.light') : t('theme.dark');
    }

    var langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        var flagSpan = langBtn.querySelector('.lang-flag');
        flagSpan.className = 'lang-flag fi ' + (lang === 'cz' ? 'fi-cz' : 'fi-gb');
        langBtn.querySelector('.lang-text').textContent = lang === 'cz' ? 'Česky' : 'English';
        langBtn.setAttribute('aria-label', t('lang.toggle.arialabel'));
    }
}

(function () {
    var saved = localStorage.getItem('lang');
    if (saved) {
        setLanguage(saved);
        return;
    }
    var langs = navigator.languages && navigator.languages.length
        ? navigator.languages
        : [navigator.language || 'en'];
    var langMatch = langs.some(function(l) {
        return l.toLowerCase().startsWith('cs') || l.toLowerCase().startsWith('sk');
    });
    if (langMatch) {
        setLanguage('cz');
        return;
    }
    try {
        var tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
        if (tz === 'Europe/Prague' || tz === 'Europe/Bratislava') {
            setLanguage('cz');
            return;
        }
    } catch (e) {}
    setLanguage('en');
}());
